/** 업종 선택에서 가장 적합한 클리셰 항목을 조회 */
export function lookupCliche(industry: IndustrySelection): ClicheEntry | null {
  const terms = [industry.minor, industry.medium, industry.major].filter(Boolean);

  for (const term of terms) {
    // 직접 매핑
    if (CLICHE_DB[term]) return CLICHE_DB[term];

    // 포함 관계 매핑
    for (const [key, entry] of Object.entries(CLICHE_DB)) {
      if (term.includes(key) || key.includes(term)) return entry;
    }
  }
  return null;
}

/** 클리셰 항목을 프롬프트 삽입용 텍스트로 변환 */
export function formatClicheForPrompt(entry: ClicheEntry): string {
  const lines: string[] = ['\n\n[업종 클리셰 금지 목록 — 아래 이름·패턴은 절대 사용하지 마세요]'];
  lines.push(`금지어: ${entry.forbidden.join(', ')}`);
  if (entry.patterns.length > 0) {
    lines.push(`금지 패턴: ${entry.patterns.join(' / ')}`);
  }
  return lines.join('\n');
}
